public class StringExercise {
    public static void main(String[] args) {

        String education = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.printf(education);
        System.out.println();

        String quotes = "Check \"this\" out!, \"s inside of \"s!";
        System.out.printf(quotes);
        System.out.println();

        String MainDrive = "In windows, the main drive is usually C:\\";
        System.out.printf(MainDrive);
        System.out.println();

        String BackSlash = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.printf(BackSlash);
        System.out.println();

    }
}
